package com.yinzhiwu.yiwu.util.beanutils;

@SuppressWarnings("serial")
public class MapedPropertyDismatchException extends RuntimeException{

	public MapedPropertyDismatchException() {
		super();
	}

	public MapedPropertyDismatchException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public MapedPropertyDismatchException(String message, Throwable cause) {
		super(message, cause);
	}

	public MapedPropertyDismatchException(String message) {
		super(message);
	}

	public MapedPropertyDismatchException(Throwable cause) {
		super(cause);
	}

	
}
