package com.yinzhiwu.yiwu.exception.business;

@SuppressWarnings("serial")
public class LessonPraiseException extends BusinessException{

	public LessonPraiseException() {
		super();
	}

	public LessonPraiseException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LessonPraiseException(String message, Throwable cause) {
		super(message, cause);
	}

	public LessonPraiseException(String message) {
		super(message);
	}

	public LessonPraiseException(Throwable cause) {
		super(cause);
	}

	
}
