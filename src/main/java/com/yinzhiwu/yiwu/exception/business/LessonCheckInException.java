package com.yinzhiwu.yiwu.exception.business;

@SuppressWarnings("serial")
public class LessonCheckInException extends BusinessException {

	public LessonCheckInException() {
		super();
	}

	public LessonCheckInException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LessonCheckInException(String message, Throwable cause) {
		super(message, cause);
	}

	public LessonCheckInException(String message) {
		super(message);
	}

	public LessonCheckInException(Throwable cause) {
		super(cause);
	}

	
	
}
