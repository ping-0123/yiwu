package com.yinzhiwu.yiwu.exception.business;

@SuppressWarnings("serial")
public class LessonInteractiveException extends BusinessException{

	public LessonInteractiveException() {
		super();
	}

	public LessonInteractiveException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LessonInteractiveException(String message, Throwable cause) {
		super(message, cause);
	}

	public LessonInteractiveException(String message) {
		super(message);
	}

	public LessonInteractiveException(Throwable cause) {
		super(cause);
	}

	
}
